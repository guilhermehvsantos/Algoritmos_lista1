package lista1;

public class Exercicio1 {

	public static void main(String[] args) {
		double A = 5;
		double B = 1; 
		double C = 3;
		double D = 8;
		
		double result1 = A * B - C + D; 
		double result2 = A * (B - C);
		double result3 = A - C * C + A;
		double result4 = D * (B/2);
		double result5 = B * D * A - C;
		double result6 = (A + B + C) * D;
		
		System.out.println("resultado 1 é " + result1);
		System.out.println("resultado 2 é " + result2);
		System.out.println("resultado 3 é " + result3);
		System.out.println("resultado 4 é " + result4);
		System.out.println("resultado 5 é " + result5);
		System.out.println("resultado 6 é " + result6);
		
	}

}
